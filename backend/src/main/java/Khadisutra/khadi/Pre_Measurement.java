package Khadisutra.khadi;

public class Pre_Measurement {
	
	private String pre_mea_id;
	private String pre_mea_name;
	
	
	public Pre_Measurement(String pre_mea_id, String pre_mea_name) {
		super();
		this.pre_mea_id = pre_mea_id;
		this.pre_mea_name = pre_mea_name;
	}


	public String getPre_mea_id() {
		return pre_mea_id;
	}


	public void setPre_mea_id(String pre_mea_id) {
		this.pre_mea_id = pre_mea_id;
	}


	public String getPre_mea_name() {
		return pre_mea_name;
	}


	public void setPre_mea_name(String pre_mea_name) {
		this.pre_mea_name = pre_mea_name;
	}
	
	
	
	
	

}
